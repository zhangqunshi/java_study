class Cast {
    public static void main(String[] args) {
        float i = 200.12f;
        long l = (long) i;
        long l2 = (long) 200;
        long l3 = 300;

        System.out.println("i = " + i);
        System.out.println("l = " + l);
        System.out.println("l2 = " + l2);
        System.out.println("l3 = " + l3);
    }
}